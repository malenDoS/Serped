   public void agnadirAgnos() throws IOException, MalformedURLException, ParserConfigurationException, XPathExpressionException, SAXException{
       this.listaAgnos=this.recurso.getItemsAgno();
       for(int i=0;i<listaAgnos.size();i++){
           this.jComboBox4.addItem((String)this.listaAgnos.get(i));
       }
       
   }
   
   public void agnadirLugar(){
       this.listaLugares=this.recurso.getItemsLugar();
       for(int i=0;i<this.listaLugares.size();i++){
           this.jComboBox1.addItem((String) this.listaLugares.get(i));
       }
   }
   
   public void agnadirGenero(){
       this.jComboBox2.addItem("Masculino");
       this.jComboBox2.addItem("Femenino");
   }
   
   public void agnadirGrado(){
       this.jComboBox3.addItem("1");
       this.jComboBox3.addItem("2");
       this.jComboBox3.addItem("3");
       
   }
   
   public void escribirResultado(ArrayList<String>resultadoPantalla){
       
       this.jTextPane1.setText("");
       String frase="\n\n\n\n\n     Año: "+resultadoPantalla.get(0)+"\n\n     Localización: "+resultadoPantalla.get(1)+"\n\n     "+resultadoPantalla.get(2)+" Grado "+resultadoPantalla.get(3)+": \n     "+resultadoPantalla.get(4)+"\n\n     TOTAL: "+resultadoPantalla.get(5)+" personas";
       Font fuentePantalla=new Font("Serif",Font.BOLD,14);
       jTextPane1.setFont(fuentePantalla);
       this.jTextPane1.setText(frase);
   }
  

