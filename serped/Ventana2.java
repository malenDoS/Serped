    private void bBuscar1realizarConsulta(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscar1realizarConsulta
        this.jTextPane1.enable();
        this.opcionesSel=new ArrayList<String>();
        if(this.jComboBox1.getSelectedItem().equals("Indefinido")||this.jComboBox2.getSelectedItem().equals("Indefinido")||this.jComboBox3.getSelectedItem().equals("Indefinido")){
            this.mensajeU.setFont(new Font("Serif",Font.BOLD,14));
            
            this.mensajeU.setText("Rellena los datos del formulario");
        }else{
            this.mensajeU.setText("");
            this.lugar=(String) this.jComboBox1.getSelectedItem();
            this.agno=(String) this.jComboBox4.getSelectedItem();
            this.genero=(String) this.jComboBox2.getSelectedItem();
            this.grado=(String)this.jComboBox3.getSelectedItem();
            this.opcionesSel.add(this.agno);
            this.opcionesSel.add(this.lugar);
            this.opcionesSel.add(this.genero);
            this.opcionesSel.add(this.grado);
        escribirResultado(this.recurso.buscarConsulta(this.opcionesSel));
        }
    }//GEN-LAST:event_bBuscar1realizarConsulta

